package main

import (
	"fmt"
	"net/http"
	"time"
)

func main() {
	links := []string{
		"http://google.com",
		"http://facebook.com",
		"http://stackoverflow.com",
		"http://golang.org",
		"http://amazon.com",
	}

	c := make(chan string)

	for _, link := range links {
		go checkLink(link, c)
	}

	/**
	for { //infinite for loop
		go checkLink(<-c, c) // awaiting channel response which causes blocking
	}
	**/

	for l := range c { // range before a channel is equivalent to <-c
		go func(url string) {
			time.Sleep(5 * time.Second)
			checkLink(url, c)
		}(l)
	}
}

func checkLink(link string, c chan string) {
	_, err := http.Get(link)
	if err != nil {
		fmt.Println(link, "might be down!")
		c <- link
		return
	}

	fmt.Println(link, "is up!")
	c <- link
}
